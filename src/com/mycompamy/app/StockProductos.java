    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
    Productos prod = new Productos();
        
        prod.setProducto(this.txtProducto.getText());
        prod.setCantidad(this.txtCantidad.getText());
        prod.setTotal(this.txtTotal.getText());
        
        lista.add(prod);
        mostrar();
        
      
            
     limpiarTexto();
    }//GEN-LAST:event_btnRegistrarActionPerformed
  private void limpiarTexto(){
    this.txtProducto.setText("");
    this.txtCantidad.setText("");
    this.txtTotal.setText("");
    }
    
   public void mostrar(){
        String matriz[][] = new String[lista.size()][4];
       for(int i= 0; i<lista.size();i++){
           matriz[i][0] += i;
           matriz[i][1] = lista.get(i).getProducto();
           matriz[i][2] = lista.get(i).getCantidad();
           matriz[i][3] = lista.get(i).getTotal();
       }
         jTableMostrar.setModel(new javax.swing.table.DefaultTableModel(
                
                 matriz,
            new String [] {
                "Codigo", "Producto", "Cantidad", "Total"
            }
         ));
                 
     
   }
    
    
